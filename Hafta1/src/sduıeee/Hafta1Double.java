public class Hafta1Double {

    public static void main(String[] args) {
        //double tanımlama ve yazdırma 
        double a = 10.5;//double tanımlanırken nokta kullanılır
        //double a=10.5D diyede tanımlanabilir 'D' double olduğunu gösterir
        System.out.println(a);
        //ikinci bir double tanımlayıp işlem yapma
        double b=3.2;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        // istenilirse işlemin sonu başka bir referansa bağlanır 
        // ve o sonuç yazdırılabilir
        //doublen bir benzeri float
        float c=10.2F; //sondaki F float olduğunu gösterir
        System.out.println(c);
    }
}
