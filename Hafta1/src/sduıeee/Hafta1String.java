
package sduıeee;
public class Hafta1String {
    
    public static void main(String[] args) {
        //Her programa dilinde olduğu gibi --Hello--
        System.out.println("Hello World!");
        //Türkkçe karakter desteği
        System.out.println("Merhaba Dünya");
        //iki ifadenin birleştirelek yazılabileceği
        System.out.println("Merhaba "+"Dünya");
        //String tanımalama ve yazdırma 
        String a="Merhaba Dünya";//String iki tırnak arasına tanımlanır
        System.out.println(a);
        //String tanımlamanın diğer bir yolu
        String b;
        b="Merhaba";
        //Stringi yazdırma yolları 
        String c="Dünya";
        System.out.println(b+" Dünya");
        System.out.println(b+c);
        //arada boşluk bırakma
        System.out.println(b+" "+c);
    }
}
